package rvcc.csc;

public class Arithmetic {
    public static void main(String[] args) {
        System.out.println( "2+2" + (2+2)    );
        System.out.println( "2+2" + (2*2)    );
        System.out.println( "2+2" + (2-2)    );
        System.out.println( "2+2" + (12/2)   );
        System.out.println( "2+2" + (12%2)   );
    }
}
